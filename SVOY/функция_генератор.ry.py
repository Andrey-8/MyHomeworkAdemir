""" создаем функцию, которая построчно считывает содержимое файла
и потом все это обрабатывает"""


def some():
    # конечно создаем пустой список, который будет принимать информацию
    list_text = []
    # теперь открываем файл для чтения
    with open("save.txt", encoding="utf-8") as r:
        # теперь добавляем содержимое файла в пустой список list_text
        # но делаем это через цикл for, для считывания информации построчно
        for x in r:
            list_text.append(x)
    return list_text


# работаем с готовым списком, с помощью цикла for

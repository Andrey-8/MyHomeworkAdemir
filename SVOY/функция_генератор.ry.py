def some2():
    with open("save.txt", encoding="utf-8") as r:
        for x in r:
            # для создания из функции объект-генератора, указывает оператор yield
            # он при срабатывании функции, будет выдавать по 1-му значению,
            # из каждого цокла for
            yield x



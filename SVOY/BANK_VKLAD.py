# Вот код на Python, который вычисляет доход с капитализацией за месяц и выводит доход по итогам месяца:

principal = 100000  # первоначальная сумма вклада
annualrate = 0.24  # годовая ставка (24% годовых)
months = 6  # количество месяцев - срок вклада

monthlyrate = (
    annualrate / 12
)  # годовая ставка (24%) / 12 (кол-во месяцев в году) = ставка ежемесячного дохода

for month in range(1, months + 1):
    income = (
        principal * monthlyrate
    )  # доход в месяц = сумма вклада * ставка (в процентах)
    principal += income  # увеличиваем сумму вклада, на ежемесячный доход
    print(f"Месяц {month}: доход = {income:.2f}, сумма вклада = {principal:.2f}")
